#include "Common.h"
#include "ModuleManagerFlash.h"

#include "FlashManager.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    ModuleManagerFlash
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
ModuleManagerFlash::ModuleManagerFlash (FlashManager & flash_manager) :
  ModuleManager (MODULE_MANAGER_FLASH),
  m_flash_manager (flash_manager)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    ModuleManagerFlash
// @method:   getFlashInterface
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Flash &
ModuleManagerFlash::getFlashInterface ()
{
  return m_flash_manager;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    ModuleManagerFlash
// @method:   doAction
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
ModuleManagerFlash::doAction (TypeModuleAction action)
{
  m_flash_manager.doAction (action);
}


