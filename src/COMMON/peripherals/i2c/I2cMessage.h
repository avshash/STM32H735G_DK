#ifndef _I2C_MESSAGE_H_
#define _I2C_MESSAGE_H_

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @struct:   I2cMessage
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct I2cMessage
{
  // Support only 7 bit device address.
  uint8_t m_device_address;
  // Support only single byte memory address.
  uint8_t m_data_address;
  uint8_t m_data_size;
  uint8_t m_message_data[16];
};

#endif  // _I2C_MESSAGE_H_
