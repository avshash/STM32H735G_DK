#include "Common.h"
#include "SystemTime.h"

uint32_t SystemTime::g_ticks_count;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    SystemTime
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
SystemTime::SystemTime ()
{
  g_ticks_count = 0;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    SystemTime
// @method:   getSystemMilliTime
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
uint32_t
SystemTime::getSystemMilliTime ()
{
  return g_ticks_count;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: sysTickHandler
// @dscr:     Systick interrupt handler.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
extern "C" void
sysTickHandler ()
{
  SystemTime::g_ticks_count++;
}

