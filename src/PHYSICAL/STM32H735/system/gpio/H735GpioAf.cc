#include "Common.h"
#include "H735GpioAf.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioAf
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioAf::H735GpioAf (TypeGpioBank bank, uint32_t pin_number)
: H735Gpio (bank, pin_number)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioAf
// @method:   destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioAf::~H735GpioAf ()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioAf
// @method:   activate
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioAf::activate (int af_index, TypeGpioSpeed speed_type, TypeGpioLoad load_type, TypeGpioOutput output_type)
{
  setAfMode (af_index);
  setOutputSpeed (speed_type);
  setLoadResistor (load_type);
  setOutputType (output_type);
  setPortMode (GPIO_MODE_AF);
}

