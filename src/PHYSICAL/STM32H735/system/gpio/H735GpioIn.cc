#include "Common.h"
#include "H735GpioIn.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioIn
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioIn::H735GpioIn (TypeGpioBank bank, int pin_number)
: H735Gpio (bank, pin_number)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioIn
// @method:   destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioIn::~H735GpioIn ()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioIn
// @method:   activate
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioIn::activate (TypeGpioLoad load_type)
{
  setPortMode (GPIO_MODE_INPUT);
  setLoadResistor (load_type);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioIn
// @method:   readValue
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool
H735GpioIn::readValue () const
{
  return H735Gpio::readValue ();
}

