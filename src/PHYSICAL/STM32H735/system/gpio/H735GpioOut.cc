#include "Common.h"
#include "H735GpioOut.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioOut::H735GpioOut (TypeGpioBank bank, int pin_number)
: H735Gpio (bank, pin_number)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735GpioOut::~H735GpioOut ()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   activatePushPull
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioOut::activatePushPull (TypeGpioSpeed speed_type, bool b_value)
{
  activate (b_value, speed_type, GPIO_LOAD_DISCONNECTED, GPIO_OUTPUT_PUSH_PULL);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   activateOpenDrain
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioOut::activateOpenDrain (TypeGpioSpeed speed_type, TypeGpioLoad load_type, bool b_value)
{
  activate (b_value, speed_type, load_type, GPIO_OUTPUT_OPEN_DRAIN);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   activate
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioOut::activate (bool b_value, TypeGpioSpeed speed_type, TypeGpioLoad load_type, TypeGpioOutput output_type)
{
  H735Gpio::setOutput (b_value);
  setOutputSpeed (speed_type);
  setLoadResistor (load_type);
  setOutputType (output_type);
  setPortMode (GPIO_MODE_OUTPUT);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735GpioOut
// @method:   setOutput
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735GpioOut::setOutput (bool b_value)
{
  H735Gpio::setOutput (b_value);
}

