#include "Common.h"
#include "H735ClockGeneration.h"

#include <cstring>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735ClockGeneration
// @method:   constructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
H735ClockGeneration::H735ClockGeneration ()
{
  memset (m_params, 0xFFU, sizeof (m_params));
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735ClockGeneration
// @method:   activate
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735ClockGeneration::activate ()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    H735ClockGeneration
// @method:   setParam
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
H735ClockGeneration::setParam (TypeClockParam param, uint32_t val)
{
  ASSERT_TEST ((m_params[param] == 0xFFFFFFFFUL) || (m_params[param] == val));
  m_params[param] = val;
}

