#include "Common.h"
#include "Disc735I2cMaster4.h"

#include <cstring>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    Disc735I2cMaster4
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Disc735I2cMaster4::Disc735I2cMaster4 () :
  PhysicalI2cMaster (I2C_4_REGISTERS),
  m_scl (GPIO_BANK_F, 14),
  m_sda (GPIO_BANK_F, 15)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    Disc735I2cMaster4
// @method:   activateGpio
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
Disc735I2cMaster4::activateGpio ()
{
  m_scl.activate (4, GPIO_SPEED_MEDIUM, GPIO_LOAD_PULL_UP, GPIO_OUTPUT_OPEN_DRAIN);
  m_sda.activate (4, GPIO_SPEED_MEDIUM, GPIO_LOAD_PULL_UP, GPIO_OUTPUT_OPEN_DRAIN);
}

