#include "Common.h"
#include "Disc735ModuleManagerLeds.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    Disc735ModuleManagerLeds
// @method:   constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Disc735ModuleManagerLeds::Disc735ModuleManagerLeds () :
  ModuleManagerLeds (),
  m_red (GPIO_BANK_C, 2),
  m_green (GPIO_BANK_C, 3)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    Disc735ModuleManagerLeds
// @method:   setLed
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
Disc735ModuleManagerLeds::setLed (TypeLedColor color, bool b_on)
{
  switch (color)
  {
    case LED_COLOR_RED:
      m_red.setOutput (!b_on);
      break;

    case LED_COLOR_GREEN:
      m_green.setOutput (!b_on);
      break;

    default:
      break;
  }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    Disc735ModuleManagerLeds
// @method:   activateGpio
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
Disc735ModuleManagerLeds::activateGpio ()
{
  m_red.activatePushPull (GPIO_SPEED_LOW, false);
  m_green.activatePushPull (GPIO_SPEED_LOW, true);
}

