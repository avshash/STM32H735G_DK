#include "common.h"
#include "StringObjectPair.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   constructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringObjectPair::StringObjectPair (const String & key, ObjectAPtr object)
: m_key (key),
  m_object (object)
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   destructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringObjectPair::~StringObjectPair ()
{
  if (m_object != NULL)
  {
    delete m_object;
  }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   getKey
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const String &
StringObjectPair::getKey () const
{
  return m_key;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   getObject
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Object &
StringObjectPair::getObject ()
{
  return *m_object;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   getObject
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const Object &
StringObjectPair::getObject () const
{
  return *m_object;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringObjectPair
// @method:   hash
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
uint32_t
StringObjectPair::hash (uint32_t mod) const
{
  return String::hash (mod);
}

