#include "common.h"
#include "StringAPtr.h"

#include "String.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringAPtr
// @method:   constructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringAPtr::StringAPtr (String * object)
: m_object (object)
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringAPtr
// @method:   constructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringAPtr::StringAPtr (StringAPtr & source)
: m_object (source.m_object)
{
  source.m_object = NULL;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringAPtr
// @method:   destructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringAPtr::~StringAPtr ()
{
  if (m_object != NULL)
  {
    delete m_object;
  }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    StringAPtr
// @method:   operator
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
StringAPtr::operator String * ()
{
  String * ret_val = m_object;
  m_object = NULL;

  return ret_val;
}



