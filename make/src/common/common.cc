#include "common.h"

#define COLOR_RED "\033[0;31m"
#define COLOR_GREEN "\033[0;32m"

namespace
{
  const char * colors[] =
  {
    "\033[0m",                      // WHITE.
    "\033[0;31m",                   // RED.
    "\033[0;34m",                   // BLUE.
    "\033[0;32m",                   // GREEN.
    "\033[0;35m",                   // PURPLE.
    "\033[1;33m"                    // YELLOW
  };
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, const char * param, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg, param);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, const char * param1, const char * param2, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg, param1, param2);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, const char * param1, int param2, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg, param1, param2);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, int param, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg, param);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @function: printError
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
printError (const char * msg, int param1, int param2, TypeErrorColor color) noexcept
{
  fprintf (stderr, colors[color]);
  fprintf (stderr, msg, param1, param2);
  fprintf (stderr, colors[ERROR_COLOR_WHITE]);
}

