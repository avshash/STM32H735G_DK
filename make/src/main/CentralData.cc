#include "common.h"
#include "CentralData.h"

String CentralData::m_paths[PATH_TYPE_ARRAY_SIZE];

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    CentralData
// @method:   appendPathName
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
CentralData::appendPathName (TypePath type, const char * postfix)
{
  INTERNAL_ASSERT ((0 <= type) && (type < PATH_TYPE_ARRAY_SIZE));
  String & path = m_paths[type];

  path << postfix;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    CentralData
// @method:   path
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const String &
CentralData::path (TypePath type)
{
  INTERNAL_ASSERT ((0 <= type) && (type < PATH_TYPE_ARRAY_SIZE));
  return m_paths[type];
}


