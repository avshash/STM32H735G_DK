#include "common.h"
#include "FileParserConfigurationRead.h"

#include "Configuration.h"
#include "String.h"
#include "FileParserCompiler.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    FileParserConfigurationRead
// @method:   constructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
FileParserConfigurationRead::FileParserConfigurationRead (Configuration & configuration, const String & file_name)
: FileParserConfiguration (configuration, file_name)
{
  parseFile ();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    FileParserConfigurationRead
// @method:   destructor
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
FileParserConfigurationRead::~FileParserConfigurationRead ()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    FileParserConfigurationRead
// @method:   readFile
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
FileParserConfigurationRead::readFile (const String & file_name)
{
  FileParserConfigurationRead file_parser (getConfiguration (), file_name);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    FileParserConfigurationRead
// @method:   registerCompiler
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
FileParserConfigurationRead::registerCompiler (const String & compiler_file)
{
  FileParserCompiler compiler_parser (getConfiguration (), compiler_file);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @class:    FileParserConfigurationRead
// @method:   activateLibrary
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void
FileParserConfigurationRead::activateLibrary (const String & library_name)
{
  Configuration & configuration = getConfiguration ();
  configuration.registerActiveLibrary (library_name);
}

